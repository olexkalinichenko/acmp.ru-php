$text = "листівку прийшлось зашифрувати, щоб ворог не здогадався, що в ній написано";

//Шифрування тексту
$cipher = strtr($text, $code);
echo "Оригінал текста : {$text}<br>Шифровка : {$cipher}<br>";

//Замінна ключів на значення
$alteration = array_flip($code);

//Дешифрування
$transformation = strtr($cipher, $alteration);
echo "Дешифровка : {$transformation}";

//Код для заміни букв
$code = array(
  'а' => '1',
  'б' => '2',
  'в' => '3',
  'г' => '4',
  'д' => '5',
  'е' => '6',
  'є' => '7',
  'ж' => '8',
  'з' => '9',
  'і' => '0',
  'й' => '#',
  'ї' => '!',
  'к' => '@',
  'л' => '$',
  'м' => '%',
  'н' => '*',
  'о' => '(',
